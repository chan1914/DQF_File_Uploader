package nets.CSV.webApplication.model;

public class CSVLine {

    private int id;
    private String name;
    private String email;
    private String country;
    private int age;

    public CSVLine(int id, String name, String email, String country, int age) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.country = country;
        this.age = age;

    }


}


